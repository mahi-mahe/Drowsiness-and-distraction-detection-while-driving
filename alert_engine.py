    "    h, w = frame.shape[:2]\n",
    "    rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)\n",
    "\n",
    "    # ── Face Mesh ──────────────────────────────────────────\n",
    "    mesh_result = face_mesh.process(rgb)\n",
    "\n",
    "    if mesh_result.multi_face_landmarks:\n",
    "        lm = mesh_result.multi_face_landmarks[0].landmark\n",
    "        ear_detector.process(lm, w, h, result)\n",
    "        mar_detector.process(lm, w, h, result)\n",
