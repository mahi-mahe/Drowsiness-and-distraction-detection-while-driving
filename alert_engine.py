    "# ============================================================\n",
    "# CELL 10 — FPS benchmark (CPU vs ONNX)\n",
    "# ============================================================\n",
    "import time\n",
    "import numpy as np\n",
    "\n",
    "def fps_benchmark(model, n_runs=100, img_size=640):\n",
    "    dummy = np.random.randint(0, 255, (img_size, img_size, 3), dtype=np.uint8)\n",
    "    \n",
    "    # Warmup\n",
    "    for _ in range(5):\n",
    "        model(dummy, verbose=False)\n",
    "    \n",
    "    times = []\n",
    "    for _ in range(n_runs):\n",
    "        t0 = time.perf_counter()\n",
    "        model(dummy, verbose=False)\n",
    "        times.append(time.perf_counter() - t0)\n",
    "    \n",
    "    times = np.array(times) * 1000  # ms\n",
    "    return {\n",
    "        'mean_ms': float(np.mean(times)),\n",
    "        'std_ms':  float(np.std(times)),\n",
    "        'fps':     float(1000 / np.mean(times)),\n",
    "        'p95_ms':  float(np.percentile(times, 95)),\n",
    "    }\n",
    "\n",
    "from ultralytics import YOLO\n",
    "\n",
    "print('Benchmarking YOLOv8n (PyTorch)...')\n",
    "pt_model = YOLO('yolov8n.pt')\n",
    "pt_bench = fps_benchmark(pt_model)\n",
    "print(f'  Mean: {pt_bench[\"mean_ms\"]:.1f}ms | FPS: {pt_bench[\"fps\"]:.1f} | P95: {pt_bench[\"p95_ms\"]:.1f}ms')\n",
