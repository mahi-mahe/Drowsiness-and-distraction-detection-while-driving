    "    \"\"\"\n",
    "    Tests multiple EAR thresholds and returns precision/recall.\n",
    "    ear_values:        list of EAR values per frame\n",
    "    ground_truth_closed: list of booleans (True = eye closed)\n",
    "    \"\"\"\n",
    "    from sklearn.metrics import precision_score, recall_score, f1_score\n",
    "    if thresholds is None:\n",
    "        thresholds = np.arange(0.15, 0.40, 0.025)\n",
    "\n",
    "    results = []\n",
    "    for thr in thresholds:\n",
    "        y_pred = [1 if e < thr else 0 for e in ear_values]\n",
    "        y_true = [1 if g else 0 for g in ground_truth_closed]\n",
    "        results.append({\n",
    "            'threshold': thr,\n",
    "            'precision': precision_score(y_true, y_pred, zero_division=0),\n",
    "            'recall':    recall_score(y_true, y_pred, zero_division=0),\n",
    "            'f1':        f1_score(y_true, y_pred, zero_division=0),\n",
